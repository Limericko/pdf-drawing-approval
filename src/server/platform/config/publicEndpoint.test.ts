import { describe, expect, it } from "vitest";
import { isPublicEndpointHostname } from "./publicEndpoint.ts";

describe("public endpoint hostname validation", () => {
  it.each([
    "s3",
    "host.docker.internal",
    "minio.local",
    "bucket.example",
    "bücher.example",
    "LOCALHOST.",
    "service.LOCAL.",
    "service.INTERNAL.",
    "service.TEST.",
    "service.INVALID.",
    "gateway.HOME.ARPA.",
    "ipv4only.arpa",
    "x.in-addr.arpa",
    "x.ip6.arpa",
    "resolver.arpa",
    "example.com",
    "example.net",
    "example.org",
    "bad_label.cloudflare.com",
    "api.openai.com..",
    "[api.openai.com]"
  ])("rejects the invalid or special-use domain %s", (hostname) => {
    expect(isPublicEndpointHostname(hostname)).toBe(false);
  });

  it.each([
    "127.0.0.2",
    "127.0.0.1.",
    "127.0.0.1..",
    "0.0.0.0",
    "100.64.0.1",
    "169.254.1.1",
    "10.1.2.3",
    "10.0.0.1.",
    "10.0.0.1..",
    "172.16.1.1",
    "192.168.1.1",
    "192.0.0.8",
    "192.0.2.1",
    "192.0.2.1.",
    "198.18.0.1",
    "198.51.100.1",
    "203.0.113.1",
    "240.0.0.1",
    "[8.8.8.8]",
    "[::1]",
    "[::1].",
    "[::1]..",
    "[::ffff:7f00:1]",
    "[64:ff9b::7f00:1]",
    "[64:ff9b::a00:1]",
    "[64:ff9b::c0a8:101]",
    "[64:ff9b:1::1]",
    "[100::1]",
    "[100:0:0:1::1]",
    "[2001::1]",
    "[2001:2::1]",
    "[2001:db8::1]",
    "[2002::1]",
    "[3fff::1]",
    "[4000::1]",
    "[5f00::1]",
    "[8000::1]",
    "[2606:4700:4700::1111].",
    "[2606:4700:4700::1111]..",
    "[fc00::1]",
    "[fe80::1]",
    "[fec0::1]",
    "[ff02::1]"
  ])("rejects the non-public hostname %s", (hostname) => {
    expect(isPublicEndpointHostname(hostname)).toBe(false);
  });

  it.each([
    "s3.ap-east-1.amazonaws.com",
    "api.openai.com",
    "127.0.0.1.nip.io",
    "bücher.de",
    "192.0.0.9",
    "192.0.0.10",
    "8.8.8.8",
    "[64:ff9b::808:808]",
    "[2001:1::1]",
    "[2001:1::2]",
    "[2001:1::3]",
    "[2001:3::1]",
    "[2001:4:112::1]",
    "[2001:20::1]",
    "[2001:30::1]",
    "[2620:4f:8000::1]",
    "[2606:4700:4700::1111]"
  ])("accepts the public hostname %s", (hostname) => {
    expect(isPublicEndpointHostname(hostname)).toBe(true);
  });
});
